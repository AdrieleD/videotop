package Modelo;

/**
 *
 * @author adriele
 */
public class Estudio {
    private String nome;
    private String sede;

    public Estudio(String nome, String sede) {
        this.nome = nome;
        this.sede = sede;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSede() {
        return sede;
    }

    public void setSede(String sede) {
        this.sede = sede;
    }

    
}
